import {  toast } from "react-toastify";


export const notifySuccess = (arg) => {
  toast.success(arg, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: true,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
  });
};


